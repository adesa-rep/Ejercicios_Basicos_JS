// **For...of**: Usa un bucle forof para recorrer todos los destinos del array. Imprime en un _console.log_ cada uno de sus valores.

const placesToTravel = [
  'Japon',
  'Venecia',
  'Murcia',
  'Santander',
  'Filipinas',
  'Madagascar'
];

for (let place of placesToTravel) {
  console.log(`destino: ${place}`);
}
